package currency;

public class GBPConverter {

    // pound to usd
    public double GBPtoUSD (double GBP){
        return GBP * 1.22;
    }

    // pound to euro
    public double GBPtoEUR (double GBP){
        return GBP * 1.15;
    }

    // pound to yen
    public double GBPtoJPY (double GBP){
        return GBP * 182.32;
    }

    // pound to rupee
    public double GBPtoRUP (double GBP){
        return GBP * 100.99;
    }

    // pound to yuan
    public double GBPtoCHY (double GBP){
        return GBP * 8.89;
    }
}
