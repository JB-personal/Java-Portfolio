package currency;

public class EURConverter {

    // euro to usd
    public double EURtoUSD (double EUR){
        return EUR * 1.06;
    }

    // euro to pound
    public double EURtoGBP (double EUR){
        return EUR * 0.87;
    }

    // euro to yen
    public double EURtoJPY (double EUR){
        return EUR * 158.78;
    }

    // euro to rupee
    public double EURtoRUP (double EUR){
        return EUR * 87.95;
    }

    // euro to yuan
    public double EURtoCHY (double EUR){
        return EUR * 7.74;
    }
}
