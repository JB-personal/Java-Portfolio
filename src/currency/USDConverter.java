package currency;

public class USDConverter {
    // usd to pound
    public double USDtoGBP (double USD){
        return USD * 0.82;
    }

    // usd to euro
    public double USDtoEUR (double USD){
        return USD * 0.94;
    }

    // usd to yen
    public double USDtoJPY (double USD){
        return USD * 149.90;
    }

    // usd to rupee
    public double USDtoRUP (double USD){
        return USD * 83.03;
    }

    // usd to yuan
    public double USDtoCHY (double USD){
        return USD * 7.31;
    }
}
